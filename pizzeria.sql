DROP SCHEMA IF EXISTS `pizzeria` ;

CREATE SCHEMA IF NOT EXISTS `pizzeria` DEFAULT CHARACTER SET utf8 ;
USE `pizzeria` ;


-- SCHEMA

-- -----------------------------------------------------
-- Table `pizzeria`.`province`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`province` (
  `id_province` INT UNSIGNED NOT NULL,
  `name` VARCHAR(45) NULL,
  PRIMARY KEY (`id_province`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`city`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`city` (
  `id_city` INT UNSIGNED NOT NULL,
  `name` VARCHAR(200) NOT NULL,
  `province` INT UNSIGNED NOT NULL,
  PRIMARY KEY (`id_city`),
  INDEX `province_idx` (`province` ASC) VISIBLE,
  CONSTRAINT `province_city`
    FOREIGN KEY (`province`)
    REFERENCES `pizzeria`.`province` (`id_province`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`client`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`client` (
  `id_client` INT NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(45) NOT NULL,
  `surname` VARCHAR(100) NOT NULL,
  `address` VARCHAR(45) NOT NULL,
  `postal_code` VARCHAR(5) NOT NULL,
  `city` INT UNSIGNED NULL,
  `province` INT UNSIGNED NULL,
  `phone` VARCHAR(9) NULL,
  PRIMARY KEY (`id_client`),
  INDEX `city_idx` (`city` ASC) VISIBLE,
  INDEX `province_idx` (`province` ASC) VISIBLE,
  CONSTRAINT `city_client`
    FOREIGN KEY (`city`)
    REFERENCES `pizzeria`.`city` (`id_city`)
    ON DELETE SET NULL
    ON UPDATE CASCADE,
  CONSTRAINT `province_client`
    FOREIGN KEY (`province`)
    REFERENCES `pizzeria`.`province` (`id_province`)
    ON DELETE SET NULL
    ON UPDATE CASCADE)
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`shop`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`shop` (
  `id_shop` INT NOT NULL,
  `address` VARCHAR(100) NOT NULL,
  `postal_code` VARCHAR(5) NOT NULL,
  `city` INT UNSIGNED NULL,
  `province` INT UNSIGNED NULL,
  PRIMARY KEY (`id_shop`),
  INDEX `city_idx` (`city` ASC) VISIBLE,
  INDEX `province_idx` (`province` ASC) VISIBLE,
  CONSTRAINT `city_shop`
    FOREIGN KEY (`city`)
    REFERENCES `pizzeria`.`city` (`id_city`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `province_shop`
    FOREIGN KEY (`province`)
    REFERENCES `pizzeria`.`province` (`id_province`)
    ON DELETE CASCADE
    ON UPDATE CASCADE)
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`employee`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`employee` (
  `id_employee` INT NOT NULL,
  `shop` INT NOT NULL,
  `name` VARCHAR(45) NOT NULL,
  `surname` VARCHAR(100) NOT NULL,
  `nif` VARCHAR(15) NOT NULL,
  `phone` VARCHAR(45) NOT NULL,
  `job` ENUM("kitchen", "delivery") NOT NULL,
  PRIMARY KEY (`id_employee`),
  INDEX `shop_idx` (`shop` ASC) VISIBLE,
  CONSTRAINT `shop_employee`
    FOREIGN KEY (`shop`)
    REFERENCES `pizzeria`.`shop` (`id_shop`)
    ON DELETE CASCADE
    ON UPDATE CASCADE)
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`category`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`category` (
  `id_category` INT NOT NULL,
  `name` VARCHAR(100) NULL,
  PRIMARY KEY (`id_category`))
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`product`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`product` (
  `id_product` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `type` ENUM("pizza", "hamburger", "drink") NOT NULL,
  `name` VARCHAR(100) NOT NULL,
  `description` LONGTEXT NULL,
  `image` VARCHAR(500) NULL,
  `price` FLOAT UNSIGNED NOT NULL,
  `category` INT NULL,
  PRIMARY KEY (`id_product`),
  INDEX `category_idx` (`category` ASC) VISIBLE,
  CONSTRAINT `category_product`
    FOREIGN KEY (`category`)
    REFERENCES `pizzeria`.`category` (`id_category`)
    ON DELETE SET NULL
    ON UPDATE CASCADE)
ENGINE = InnoDB;


-- -----------------------------------------------------
-- Table `pizzeria`.`order`
-- -----------------------------------------------------
CREATE TABLE IF NOT EXISTS `pizzeria`.`order` (
  `id_order` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `order_datetime` TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `product_1` INT UNSIGNED NOT NULL,
  `product_1_number` TINYINT UNSIGNED NOT NULL,
  `product_2` INT UNSIGNED NULL,
  `product_2_number` TINYINT UNSIGNED NULL,
  `product_3` INT UNSIGNED NULL,
  `product_3_number` TINYINT UNSIGNED NULL,
  `price` FLOAT NOT NULL,
  `type` ENUM("delivery", "take away") NOT NULL,
  `client` INT NOT NULL,
  `shop` INT NOT NULL,
  `driver` INT NULL,
  `delivery_datetime` DATETIME NULL,
  PRIMARY KEY (`id_order`),
  INDEX `client_idx` (`client` ASC) VISIBLE,
  INDEX `shop_idx` (`shop` ASC) VISIBLE,
  INDEX `delivery_idx` (`driver` ASC) VISIBLE,
  INDEX `product1_idx` (`product_1` ASC) VISIBLE,
  INDEX `product2_idx` (`product_2` ASC) VISIBLE,
  INDEX `product3_idx` (`product_3` ASC) VISIBLE,
  CONSTRAINT `client_order`
    FOREIGN KEY (`client`)
    REFERENCES `pizzeria`.`client` (`id_client`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `shop_order`
    FOREIGN KEY (`shop`)
    REFERENCES `pizzeria`.`shop` (`id_shop`)
    ON DELETE CASCADE
    ON UPDATE CASCADE,
  CONSTRAINT `delivery_oder`
    FOREIGN KEY (`driver`)
    REFERENCES `pizzeria`.`employee` (`id_employee`)
    ON DELETE SET NULL
    ON UPDATE CASCADE,
  CONSTRAINT `product1_oder`
    FOREIGN KEY (`product_1`)
    REFERENCES `pizzeria`.`product` (`id_product`)
    ON DELETE RESTRICT
    ON UPDATE CASCADE,
  CONSTRAINT `product2_oder`
    FOREIGN KEY (`product_2`)
    REFERENCES `pizzeria`.`product` (`id_product`)
    ON DELETE SET NULL
    ON UPDATE CASCADE,
  CONSTRAINT `product3_oder`
    FOREIGN KEY (`product_3`)
    REFERENCES `pizzeria`.`product` (`id_product`)
    ON DELETE SET NULL
    ON UPDATE CASCADE)
ENGINE = InnoDB;



